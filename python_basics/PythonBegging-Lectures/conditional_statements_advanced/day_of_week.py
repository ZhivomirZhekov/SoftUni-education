# Input
day_of_week = int(input())

# Logic

if day_of_week == 1:
    print(f'Monday')
elif day_of_week == 2:
    print(f'Tuesday')
elif day_of_week == 3:
    print(f'Wednesday')
elif day_of_week == 4:
    print(f'Thursday')
elif day_of_week == 5:
    print(f'Friday')
elif day_of_week == 6:
    print(f'Saturday')
elif day_of_week == 7:
    print(f'Sunday')
else:
    print(f'Error')